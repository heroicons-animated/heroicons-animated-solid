import type { JSX } from "solid-js";
import { createSignal, mergeProps, splitProps } from "solid-js";
import { Motion } from "solid-motionone";
import { resolveTransition, resolveValues } from "@/lib/motion-compat";
import { cn } from "@/lib/utils";

export interface EllipsisHorizontalIconHandle {
  startAnimation: () => void;
  stopAnimation: () => void;
}

interface EllipsisHorizontalIconProps
  extends JSX.HTMLAttributes<HTMLDivElement> {
  size?: number;
  ref?: (handle: EllipsisHorizontalIconHandle) => void;
}

const DOT_VARIANTS = {
  normal: {
    scale: 1,
  },
  animate: (custom: number) => ({
    scale: [1, 1.3, 1],
    transition: {
      duration: 0.4,
      delay: custom * 0.05,
      ease: "easeInOut",
    },
  }),
};
const EllipsisHorizontalIcon = (rawProps: EllipsisHorizontalIconProps) => {
  const props = mergeProps({ size: 28 }, rawProps);
  const [local, others] = splitProps(props, [
    "onMouseEnter",
    "onMouseLeave",
    "class",
    "size",
    "ref",
  ]);
  const [variant, setVariant] = createSignal("normal");
  let isControlled = false;

  if (local.ref) {
    isControlled = true;
    local.ref({
      startAnimation: () => setVariant("animate"),
      stopAnimation: () => setVariant("normal"),
    });
  }

  const handleMouseEnter: JSX.EventHandler<HTMLDivElement, MouseEvent> = (
    e
  ) => {
    if (isControlled) {
      if (typeof local.onMouseEnter === "function") {
        local.onMouseEnter(e);
      }
    } else {
      setVariant("animate");
    }
  };

  const handleMouseLeave: JSX.EventHandler<HTMLDivElement, MouseEvent> = (
    e
  ) => {
    if (isControlled) {
      if (typeof local.onMouseLeave === "function") {
        local.onMouseLeave(e);
      }
    } else {
      setVariant("normal");
    }
  };

  return (
    <div
      class={cn(local.class)}
      onMouseEnter={handleMouseEnter}
      onMouseLeave={handleMouseLeave}
      {...others}
    >
      <svg
        fill="none"
        height={local.size}
        stroke="currentColor"
        stroke-linecap="round"
        stroke-linejoin="round"
        stroke-width="1.5"
        viewBox="0 0 24 24"
        width={local.size}
        xmlns="http://www.w3.org/2000/svg"
      >
        {[
          {
            d: "M6.75 12C6.75 12.4142 6.41421 12.75 6 12.75C5.58579 12.75 5.25 12.4142 5.25 12C5.25 11.5858 5.58579 11.25 6 11.25C6.41421 11.25 6.75 11.5858 6.75 12Z",
            index: 0,
          },
          {
            d: "M12.75 12C12.75 12.4142 12.4142 12.75 12 12.75C11.5858 12.75 11.25 12.4142 11.25 12C11.25 11.5858 11.5858 11.25 12 11.25C12.4142 11.25 12.75 11.5858 12.75 12Z",
            index: 1,
          },
          {
            d: "M18.75 12C18.75 12.4142 18.4142 12.75 18 12.75C17.5858 12.75 17.25 12.4142 17.25 12C17.25 11.5858 17.5858 11.25 18 11.25C18.4142 11.25 18.75 11.5858 18.75 12Z",
            index: 2,
          },
        ].map((dot) => (
          <Motion.path
            animate={resolveValues(DOT_VARIANTS, variant())}
            custom={dot.index}
            d={dot.d}
            key={dot.index}
            transition={resolveTransition(DOT_VARIANTS, variant())}
          />
        ))}
      </svg>
    </div>
  );
};

export { EllipsisHorizontalIcon };
